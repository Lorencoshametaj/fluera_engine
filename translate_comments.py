def process_file(filepath):
    """Process a single file using regex to target only comments and perform single-pass replacement."""
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
    except (UnicodeDecodeError, FileNotFoundError):
        return False
    
    original = content
    
    # 1. Build a single regex for all phrases
    # Sort by length desc to prioritize longer matches
    phrase_map = {p[0]: p[1] for p in PHRASES}
    sorted_phrases = sorted(phrase_map.keys(), key=len, reverse=True)

    if not sorted_phrases:
        return False

    # Escape phrases to be safe in regex
    # Add smart boundaries to prevent partial word matches (e.g. Invalida matching inside Invalidate)
    regex_parts = []
    for p in sorted_phrases:
        escaped = re.escape(p)
        # Check if boundary is needed
        prefix = r'\b' if p and (p[0].isalnum() or p[0] == '_') else ''
        suffix = r'\b' if p and (p[-1].isalnum() or p[-1] == '_') else ''
        regex_parts.append(prefix + escaped + suffix)

    phrase_regex = re.compile('|'.join(regex_parts))

    def replace_phrases(text):
        return phrase_regex.sub(lambda m: phrase_map[m.group(0)], text)

    # 2. Apply to comments only
    pattern = re.compile(
        r"("
        r"(?P<str3>'''(?:.|\n)*?'''|\"\"\"(?:.|\n)*?\"\"\")|"
        r"(?P<str1>'(?:\\.|[^'\n\\])*'|\"(?:\\.|[^\"\n\\])*\")|"
        r"(?P<comment_multi>/\*(?:.|\n)*?\*/)|"
        r"(?P<comment_single>//[^\n]*)"
        r")",
        re.MULTILINE
    )

    def token_replacer(match):
        group = match.group()
        if match.group('comment_multi') or match.group('comment_single'):
            # Apply single-pass replacement on the comment content
            return replace_phrases(group)
        else:
            return group

    new_content = pattern.sub(token_replacer, content)

    if new_content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(new_content)
        return True
    return False


def find_dart_files(directories):
    dart_files = []
    for dir_path in directories:
        for root, dirs, files in os.walk(dir_path):
            for f in files:
                if f.endswith('.dart'):
                    dart_files.append(os.path.join(root, f))
    return sorted(dart_files)


def main():
    directories = [
        os.path.join(BASE, 'lib'),
        os.path.join(BASE, 'test'),
    ]
    dart_files = find_dart_files(directories)
    print(f"Found {len(dart_files)} Dart files to process")
    
    modified = 0
    for fp in dart_files:
        if process_file(fp):
            relpath = os.path.relpath(fp, BASE)
            print(f"  ✓ {relpath}")
            modified += 1
    
    print(f"\nDone! Modified {modified} files.")


if __name__ == '__main__':
    main()
